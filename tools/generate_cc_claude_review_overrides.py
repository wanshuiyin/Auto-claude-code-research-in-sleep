#!/usr/bin/env python3
"""Generate Claude-review overrides for the Claude Code skill path.

Transforms skills/ (Claude Code executor + Codex MCP reviewer)
into skills-cc-claude-review/ (Claude Code executor + Claude CLI reviewer).

Usage:
    python3 tools/generate_cc_claude_review_overrides.py
"""

from __future__ import annotations

import ast
import re
import shutil
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parents[1]
SRC_ROOT = REPO_ROOT / "skills"
DEST_ROOT = REPO_ROOT / "skills" / "skills-cc-claude-review"

# All skills that reference mcp__codex__codex (auto-detected at runtime)
# These are excluded because they are overlay directories, not active skills:
EXCLUDE_DIRS = {
    "skills-codex",
    "skills-codex-claude-review",
    "skills-codex-gemini-review",
    "skills-cc-claude-review",
    "shared-references",
}

FRONTMATTER_RE = re.compile(r"^---\n(.*?)\n---\n?", re.DOTALL)

# Patterns for code blocks using Codex MCP
CODEX_BLOCK_RE = re.compile(
    r"```(?:\w*)?\n(mcp__codex__codex(?:-reply)?:[\s\S]*?)```"
)

OVERRIDE_NOTE = (
    "> Override for Claude Code users who want **Claude CLI** as the reviewer "
    "instead of Codex MCP (GPT). Generated by `tools/generate_cc_claude_review_overrides.py`."
)

REVIEWER_LINE = (
    "- **REVIEWER_MODEL = `claude-review`** -- Claude reviewer invoked through "
    "the local `claude-review` MCP bridge. Set `CLAUDE_REVIEW_MODEL` env var "
    "for a specific Claude model override."
)

ASYNC_POLLING_NOTE = (
    "After this call, save the returned `jobId` and poll "
    "`mcp__claude-review__review_status` with `waitSeconds=60` until `done=true`. "
    "**Be patient** — the reviewer has tools (WebSearch, Read, etc.) and may spend "
    "several minutes searching literature or reading files. Do NOT assume timeout "
    "until at least 20 polling attempts. "
    "The completed payload contains `response` (reviewer output) and `threadId` "
    "(for follow-up rounds)."
)

THREAD_FALLBACK_NOTE = (
    "**Thread expiration fallback**: If `review_reply_start` fails with "
    "\"session not found\" or similar error, fall back to `review_start` with "
    "the full accumulated context (include all prior round summaries in the prompt). "
    "This can happen when the reviewer CLI session expires between rounds."
)

PREREQ_BLOCK = """## Prerequisites

- **claude-review MCP** registered in Claude Code:
  ```bash
  claude mcp add claude-review -s user -- python3 /path/to/mcp-servers/claude-review/server.py
  ```
- This gives Claude Code access to `mcp__claude-review__review_start`, `mcp__claude-review__review_reply_start`, and `mcp__claude-review__review_status`.
- Optionally set model: `claude mcp add claude-review -s user -e CLAUDE_REVIEW_MODEL=claude-opus-4-1 -- python3 /path/to/server.py`"""


def resolve_source(skill_dir: Path) -> Path | None:
    """Return the Codex-based source file for a skill.

    Prefers .codex-bak (original backup) over SKILL.md, since SKILL.md may
    already be the Claude overlay from a previous run.
    """
    bak = skill_dir / "SKILL.md.codex-bak"
    if bak.exists():
        return bak
    md = skill_dir / "SKILL.md"
    if md.exists() and "mcp__codex__codex" in md.read_text(encoding="utf-8"):
        return md
    return None


def find_target_skills() -> list[str]:
    """Auto-detect all skills that have a Codex-based source."""
    targets = []
    for skill_dir in sorted(SRC_ROOT.iterdir()):
        if not skill_dir.is_dir():
            continue
        if skill_dir.name in EXCLUDE_DIRS:
            continue
        if resolve_source(skill_dir) is not None:
            targets.append(skill_dir.name)
    return targets


def extract_field(frontmatter: str, field: str) -> str:
    pattern = re.compile(rf"^{re.escape(field)}:\s*(.+)$", re.MULTILINE)
    match = pattern.search(frontmatter)
    if not match:
        return ""
    value = match.group(1).strip()
    if (value.startswith('"') and value.endswith('"')) or (
        value.startswith("'") and value.endswith("'")
    ):
        try:
            value = ast.literal_eval(value)
        except (SyntaxError, ValueError):
            value = value[1:-1]
    return value


def transform_frontmatter(frontmatter: str) -> str:
    """Rewrite frontmatter: swap tool names and update description."""
    # Replace allowed-tools references
    frontmatter = frontmatter.replace(
        "mcp__codex__codex, mcp__codex__codex-reply",
        "mcp__claude-review__review_start, mcp__claude-review__review_reply_start, mcp__claude-review__review_status",
    )
    frontmatter = frontmatter.replace(
        "mcp__codex__codex-reply",
        "mcp__claude-review__review_reply_start, mcp__claude-review__review_status",
    )
    frontmatter = frontmatter.replace(
        "mcp__codex__codex",
        "mcp__claude-review__review_start, mcp__claude-review__review_status",
    )

    # Update description text
    frontmatter = frontmatter.replace("via Codex MCP", "via claude-review MCP")
    frontmatter = frontmatter.replace("Codex MCP evaluates", "Claude reviewer evaluates")
    frontmatter = frontmatter.replace("via GPT-5.4 xhigh review", "via Claude review through claude-review MCP")
    frontmatter = frontmatter.replace("from GPT", "from Claude")

    return frontmatter


def rewrite_codex_block(match: re.Match[str]) -> str:
    """Rewrite a mcp__codex__codex code block to use claude-review MCP."""
    block_content = match.group(1)
    lines = block_content.splitlines()

    is_reply = lines[0].strip().startswith("mcp__codex__codex-reply")

    if is_reply:
        tool_name = "mcp__claude-review__review_reply_start"
    else:
        tool_name = "mcp__claude-review__review_start"

    out = [f"```\n{tool_name}:"]
    for line in lines[1:]:
        stripped = line.strip()
        # Skip config and model lines (not applicable to claude-review)
        if stripped.startswith("config:") or stripped.startswith("model:"):
            continue
        out.append(line)
    out.append("```")

    result = "\n".join(out)
    # Append async polling note
    result += f"\n\n{ASYNC_POLLING_NOTE}"
    return result


def transform_body(text: str) -> str:
    """Transform the skill body text."""
    # Rewrite code blocks first
    text = CODEX_BLOCK_RE.sub(rewrite_codex_block, text)

    # Text replacements for inline references
    text = text.replace("Codex MCP", "claude-review MCP")
    text = text.replace("via Codex", "via Claude reviewer")
    text = text.replace("to Codex for", "to Claude reviewer for")
    text = text.replace("Send the collected results to Codex", "Send the collected results to Claude reviewer")
    text = text.replace("Codex judgment", "Claude reviewer judgment")
    text = text.replace("Codex response", "Claude reviewer response")
    text = text.replace("from Codex", "from Claude reviewer")
    text = text.replace("Codex Judgment", "Claude Reviewer Judgment")
    # Role-level "Codex" references (e.g., ablation-planner: "Codex designs ablations")
    text = text.replace("Codex designs", "Claude reviewer designs")
    text = text.replace("Codex leads", "Claude reviewer leads")
    text = text.replace("Codex sees", "Claude reviewer sees")
    text = text.replace("Codex thinks", "Claude reviewer thinks")
    text = text.replace("asks Codex", "asks Claude reviewer")
    text = text.replace("ask Codex", "ask Claude reviewer")
    text = text.replace("Codex to confirm", "Claude reviewer to confirm")
    text = text.replace("Codex to re-prioritize", "Claude reviewer to re-prioritize")
    # Section headings and role descriptions
    text = text.replace("Codex Designs Ablations", "Claude Reviewer Designs Ablations")
    text = text.replace("Codex is the judge", "Claude reviewer is the judge")
    text = text.replace("Codex evaluates", "Claude reviewer evaluates")
    text = text.replace("for all Codex review calls", "for all Claude review calls")
    text = text.replace("(Codex/Claude reviewer)", "(Claude reviewer)")
    text = text.replace("(Codex, same thread)", "(Claude reviewer, same thread)")
    # Catch remaining standalone "Codex" that refer to the reviewer role
    text = text.replace("Codex review", "Claude review")
    text = text.replace("escalate to Codex", "escalate to Claude reviewer")
    text = text.replace("If Codex says", "If Claude reviewer says")
    text = text.replace("calling Codex directly", "calling Claude reviewer directly")
    text = text.replace("Codex threadId", "Claude reviewer threadId")
    text = text.replace("Codex call", "Claude reviewer call")
    # Catch-all: remaining "Codex" that clearly refers to the reviewer
    text = re.sub(r"Phase \d+ \(Codex\b", lambda m: m.group(0).replace("Codex", "Claude reviewer"), text)
    text = re.sub(r"\(Codex/", "(Claude reviewer/", text)

    # GPT model references
    text = text.replace("GPT-5.4 xhigh", "Claude reviewer")
    text = text.replace("GPT-5.4", "Claude reviewer")
    text = text.replace("gpt-5.4", "claude-review")
    text = text.replace("gpt-4o", "claude-review")

    # Inline tool references (not in code blocks)
    text = text.replace("`mcp__codex__codex-reply`", "`mcp__claude-review__review_reply_start`")
    text = text.replace("`mcp__codex__codex`", "`mcp__claude-review__review_start`")
    text = text.replace("mcp__codex__codex-reply", "mcp__claude-review__review_reply_start")
    text = text.replace("mcp__codex__codex", "mcp__claude-review__review_start")

    # Reasoning effort references
    text = text.replace("xhigh reasoning", "high-rigor review")
    text = text.replace("xhigh", "high-rigor")
    text = text.replace('{"model_reasoning_effort": "high"}', "")

    # Clean up standalone config blocks that reference reasoning effort
    text = re.sub(
        r"```\nconfig:\s*\{[^}]*model_reasoning_effort[^}]*\}\n```",
        "",
        text,
    )
    # Clean up inline config references
    text = re.sub(
        r'config:\s*\{"model_reasoning_effort":\s*"[^"]*"\}',
        "",
        text,
    )

    # Clean up empty backtick references like "ALWAYS use `` for ..."
    text = re.sub(r"ALWAYS use `` for[^\n]*", "ALWAYS request strict, high-rigor feedback from the Claude reviewer.", text)
    # Also catch "**ALWAYS use ``**" variants
    text = re.sub(r"\*{0,2}ALWAYS use ``\*{0,2}[^\n]*", "ALWAYS request strict, high-rigor feedback from the Claude reviewer.", text)

    # Fix old polling waitSeconds references
    text = text.replace("waitSeconds=20", "waitSeconds=60")

    # Add thread fallback note near review_reply_start references
    text = re.sub(
        r"(use `mcp__claude-review__review_reply_start` with the saved (?:completed )?`?threadId`?[^\n]*)",
        r"\1\n\n" + THREAD_FALLBACK_NOTE,
        text,
        count=1,
    )

    # REVIEWER_MODEL constant line
    text = re.sub(
        r"^-\s+\*{0,2}REVIEWER_MODEL\s*=.*$",
        REVIEWER_LINE,
        text,
        flags=re.MULTILINE,
    )

    # Prerequisites block: replace Codex MCP setup with claude-review setup
    text = re.sub(
        r"## Prerequisites\n\n(?:- \*{0,2}.*(?:\n(?!##).*)*)",
        PREREQ_BLOCK + "\n",
        text,
        count=1,
    )

    # threadId references are the same for both backends, no change needed

    return text


def build_frontmatter(name: str, description: str, allowed_tools: str) -> str:
    safe_desc = description.replace('"', '\\"')
    lines = [f'---', f'name: "{name}"']
    lines.append(f'description: "{safe_desc}"')
    if allowed_tools:
        lines.append(f"allowed-tools: {allowed_tools}")
    lines.append("---\n\n")
    return "\n".join(lines)


def generate_one(skill_name: str) -> None:
    skill_path = resolve_source(SRC_ROOT / skill_name)
    if skill_path is None:
        raise ValueError(f"No Codex-based source found for: {skill_name}")
    content = skill_path.read_text(encoding="utf-8")
    match = FRONTMATTER_RE.match(content)
    if not match:
        raise ValueError(f"Missing frontmatter: {skill_path}")

    frontmatter = match.group(1)
    body = content[match.end():].lstrip("\n")

    # Extract and transform frontmatter fields
    name = extract_field(frontmatter, "name") or skill_name
    description = extract_field(frontmatter, "description") or ""
    description = description.replace("via Codex MCP", "via claude-review MCP")
    description = description.replace("Codex MCP evaluates", "Claude reviewer evaluates")
    description = description.replace("via GPT-5.4 xhigh review", "via Claude review")
    description = description.replace("from GPT", "from Claude")
    description = description.replace("GPT-5.4", "Claude")
    description = description.replace("Codex designs", "Claude reviewer designs")

    allowed_tools = extract_field(frontmatter, "allowed-tools")
    if allowed_tools:
        allowed_tools = allowed_tools.replace(
            "mcp__codex__codex, mcp__codex__codex-reply",
            "mcp__claude-review__review_start, mcp__claude-review__review_reply_start, mcp__claude-review__review_status",
        )
        allowed_tools = allowed_tools.replace(
            "mcp__codex__codex-reply",
            "mcp__claude-review__review_reply_start, mcp__claude-review__review_status",
        )
        allowed_tools = allowed_tools.replace(
            "mcp__codex__codex",
            "mcp__claude-review__review_start, mcp__claude-review__review_status",
        )

    argument_hint = extract_field(frontmatter, "argument-hint")

    # Build new frontmatter
    safe_desc = description.replace('"', '\\"')
    fm_lines = ["---", f'name: "{name}"', f'description: "{safe_desc}"']
    if argument_hint:
        fm_lines.append(f"argument-hint: {argument_hint}")
    if allowed_tools:
        fm_lines.append(f"allowed-tools: {allowed_tools}")
    fm_lines.append("---\n\n")
    new_frontmatter = "\n".join(fm_lines)

    # Transform body
    new_body = OVERRIDE_NOTE + "\n\n" + transform_body(body)

    output = new_frontmatter + new_body.rstrip() + "\n"

    target_dir = DEST_ROOT / skill_name
    if target_dir.exists():
        shutil.rmtree(target_dir)
    target_dir.mkdir(parents=True, exist_ok=True)
    (target_dir / "SKILL.md").write_text(output, encoding="utf-8")
    print(f"  Generated: {skill_name}")


def generate_readme() -> None:
    targets = find_target_skills()
    readme = f"""# skills-cc-claude-review

Override layer for **Claude Code** users who want **Claude CLI** as the external
reviewer instead of Codex MCP (GPT).

## Architecture

- **Executor**: Claude Code (your current session)
- **Reviewer**: A fresh Claude CLI instance, invoked through the `claude-review` MCP bridge
- **Transport**: `mcp-servers/claude-review/server.py`

Flow:
```
Claude Code (executor) -> claude-review MCP -> fresh Claude CLI -> Claude API
```

## What this package contains

Override skills ({len(targets)} total):
"""
    for t in targets:
        readme += f"- `{t}`\n"

    readme += """
## Install

```bash
# 1. Switch to Claude reviewer backend
./tools/switch_reviewer.sh claude

# 2. Or manually: register MCP + copy skills
claude mcp add claude-review -s user -- python3 $(pwd)/mcp-servers/claude-review/server.py
cp -a skills/skills-cc-claude-review/* skills/
```

## Switch back to Codex

```bash
./tools/switch_reviewer.sh codex
```

## Regenerate

```bash
python3 tools/generate_cc_claude_review_overrides.py
```
"""
    (DEST_ROOT / "README.md").write_text(readme, encoding="utf-8")
    print(f"  Generated: README.md")


def main() -> None:
    targets = find_target_skills()
    print(f"Found {len(targets)} skills to transform:")
    for t in targets:
        print(f"  - {t}")
    print()

    DEST_ROOT.mkdir(parents=True, exist_ok=True)
    for skill_name in targets:
        generate_one(skill_name)

    generate_readme()
    print(f"\nDone. Output: {DEST_ROOT.relative_to(REPO_ROOT)}/")


if __name__ == "__main__":
    main()
